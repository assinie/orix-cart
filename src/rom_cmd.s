.import PrintHexByte

;----------------------------------------------------------------------
;                               Exports
;----------------------------------------------------------------------

;----------------------------------------------------------------------
; Defines / Constants
;----------------------------------------------------------------------
        twil_register := $0342
        twil_bank     := $0343

        V2DRA := $321


;----------------------------------------------------------------------
;                               Page Zéro
;----------------------------------------------------------------------


;----------------------------------------------------------------------
; Définition de la rom
;----------------------------------------------------------------------
        ;----------------------------------------------------------------------
        ; Liste des commandes
        ;----------------------------------------------------------------------
        add_command "hello"
        add_command "bonjour"
        add_command "bankid"

        ;----------------------------------------------------------------------
        ; Vecteurs Orix: rom_type, parse_vector, rom_signature
        ;----------------------------------------------------------------------
        set_orix_vectors $01, $0000, "Dummy ROM"
        ; set_orix_vectors $01, $0000, rom_signature

        ;----------------------------------------------------------------------
        ; Vecteurs 6502: nmi, reset, irq
        ;----------------------------------------------------------------------
        set_cpu_vectors rom_start, rom_start, IRQVECTOR

        ;----------------------------------------------------------------------
        ; Signature de la rom
        ;----------------------------------------------------------------------
        ; rom_signature: .asciiz "Example ROM"


;----------------------------------------------------------------------
;                            Code de la rom
;----------------------------------------------------------------------
rom_start
        print startup_message, NOSAVE
        rts

hello
        print hello_world, NOSAVE
        rts

bonjour
        print bonjour_monde, NOSAVE
        rts

bankid
        print bank_id, NOSAVE
        lda twil_bank
        asl
        asl
        tsx
        pha

        lda V2DRA
        and #$07
        clc
        adc $100,x
        sta $100,x

        lda twil_register
        and #$20
        adc $100,x

        jsr PrintHexByte
        BRK_KERNEL XCRLF

        pla
        rts

;----------------------------------------------------------------------
;                       Chaines de caractères
;----------------------------------------------------------------------
startup_message:
        .asciiz "Starting romm..."

hello_world:
        .asciiz "Hello world!\r\n"

bonjour_monde:
        .asciiz "Bonjour le monde!\r\n"

bank_id:
        .asciiz "Bank $"
